/**
 * op_add - func add two numbers
 * @a: input number
 * @b: input number
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - func subtract two numbers
 * @a: input number
 * @b: input number
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_div - func divide two numbers
 * @a: input number
 * @b: input number
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - func module two numbers
 * @a: input number
 * @b: input number
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
/**
 * op_mul - func multiply two numbers
 * @a: input number
 * @b: input number
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
